mark=int(input('Enter your  mark '))
if mark>90:
    print('The grade you have gotten is A')
elif mark>80:
    print('The grade you have gotten is B')
elif mark>70:
    print('The grade you have gotten is C')
elif mark>60:
    print('The grade you have gotten is D')
else:
    print('You failed, the grade you have gotten is F')